import SwiftUI

extension Color {
    
    // Background
    static let primaryBackgroundColor = Color(UIColor(named: "PrimaryBackgroundColor")!)
    static let secondaryBackgroundColor = Color(UIColor(named: "SecondaryBackgroundColor")!)

    // Text
    static let primaryTextColor = Color(UIColor(named: "PrimaryTextColor")!)
    static let secondaryTextColor = Color(UIColor(named: "SecondaryTextColor")!)

    // Search
    static let searchFieldBackgroundColor = Color(UIColor(named: "SearchFieldBackgroundColor")!)
    static let searchFieldIconTintColor = Color(UIColor(named: "SearchFieldIconTintColor")!)
    static let searchFieldTextColor = Color(UIColor(named: "SearchFieldTextColor")!)
}
